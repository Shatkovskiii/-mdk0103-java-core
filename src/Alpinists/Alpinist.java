package Alpinists;

public class Alpinist {
    String name;
    int age;
    String address;

    // Конструктор для создания объекта альпиниста с  именем возрастом и адресом
    public Alpinist(String name, int age, String address) {
        this.name = name;
        this.age = age;
        this.address = address;
    }
}